CREATE SCHEMA IF NOT EXISTS metadata;
CREATE SCHEMA IF NOT EXISTS bronze;
CREATE SCHEMA IF NOT EXISTS silver;
CREATE SCHEMA IF NOT EXISTS gold;

COMMENT ON SCHEMA metadata IS 'Pipeline run metadata, data quality reports, and data contracts.';
COMMENT ON SCHEMA bronze IS 'Raw-preserved lakehouse table representations.';
COMMENT ON SCHEMA silver IS 'Clean staged lakehouse table representations.';
COMMENT ON SCHEMA gold IS 'Curated facts, dimensions, OBTs, and feature tables.';

CREATE TABLE IF NOT EXISTS metadata.pipeline_runs (
    pipeline_run_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pipeline_name TEXT NOT NULL,
    run_id TEXT NOT NULL UNIQUE,
    layer_name TEXT NOT NULL CHECK (layer_name IN ('bronze', 'silver', 'gold', 'streaming')),
    status TEXT NOT NULL CHECK (status IN ('running', 'success', 'failed', 'skipped')),
    started_at TIMESTAMPTZ NOT NULL,
    finished_at TIMESTAMPTZ,
    source_system TEXT,
    source_path TEXT,
    target_table TEXT,
    row_count BIGINT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS metadata.data_quality_reports (
    quality_report_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pipeline_run_id BIGINT REFERENCES metadata.pipeline_runs (pipeline_run_id),
    layer_name TEXT NOT NULL CHECK (layer_name IN ('bronze', 'silver', 'gold', 'streaming')),
    table_name TEXT NOT NULL,
    report_path TEXT,
    row_count BIGINT,
    issue_count BIGINT,
    status TEXT NOT NULL CHECK (status IN ('passed', 'warning', 'failed')),
    report_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS metadata.data_contracts (
    data_contract_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    schema_name TEXT NOT NULL,
    table_name TEXT NOT NULL,
    contract_version TEXT NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT true,
    contract_json JSONB NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (schema_name, table_name, contract_version)
);

CREATE TABLE IF NOT EXISTS bronze.raw_transaction_ingest_runs (
    ingest_run_id TEXT PRIMARY KEY,
    source_system TEXT NOT NULL,
    source_dataset TEXT NOT NULL,
    source_path TEXT NOT NULL,
    manifest_path TEXT,
    ingest_date DATE NOT NULL,
    started_at TIMESTAMPTZ,
    completed_at TIMESTAMPTZ,
    row_count BIGINT NOT NULL DEFAULT 0,
    source_file_count BIGINT NOT NULL DEFAULT 0,
    status TEXT NOT NULL CHECK (status IN ('running', 'success', 'failed')),
    summary_json JSONB NOT NULL DEFAULT '{}'::JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

