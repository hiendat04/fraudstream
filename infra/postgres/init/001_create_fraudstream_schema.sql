CREATE OR REPLACE VIEW gold.obt_transaction_enriched AS
SELECT
    fact.transaction_id,
    fact.event_time,
    fact.event_date,
    fact.amount,
    fact.currency,
    fact.transaction_status,
    fact.is_approved,
    fact.is_declined,
    fact.is_reversed,
    fact.is_fraud,
    fact.customer_id,
    customer.primary_city AS customer_primary_city,
    customer.account_count AS customer_account_count,
    customer.fraud_rate AS customer_fraud_rate,
    fact.account_id,
    account.transaction_count AS account_transaction_count,
    account.lifetime_amount AS account_lifetime_amount,
    fact.merchant_id,
    merchant.merchant_category,
    merchant.primary_city AS merchant_primary_city,
    merchant.fraud_rate AS merchant_fraud_rate,
    fact.channel,
    channel.channel_group,
    channel.is_digital,
    channel.is_card_present,
    fact.quality_status,
    fact.quality_issue_count,
    fact.duplicate_record_count,
    fact._gold_processed_at
FROM gold.fact_transactions AS fact
JOIN gold.dim_customer AS customer
    ON fact.customer_key = customer.customer_key
JOIN gold.dim_account AS account
    ON fact.account_key = account.account_key
JOIN gold.dim_merchant AS merchant
    ON fact.merchant_key = merchant.merchant_key
JOIN gold.dim_channel AS channel
    ON fact.channel_key = channel.channel_key;

