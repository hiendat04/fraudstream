    bronze_record_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    transaction_id TEXT,
    account_id TEXT,
    customer_id TEXT,
    merchant_id TEXT,
    merchant_category TEXT,
    amount TEXT,
    currency TEXT,
    city TEXT,
    channel TEXT,
    transaction_status TEXT,
    is_fraud TEXT,
    event_timestamp TEXT,
    created_ts TEXT,
    device_id TEXT,
    ip_address TEXT,
    authentication_method TEXT,
    risk_signal_version TEXT,
    _source_system TEXT NOT NULL,
    _source_dataset TEXT NOT NULL,
    _source_file_path TEXT NOT NULL,
